<?php

// Everything in php is expression
// An expression is a combination of values, variables, operators, and function calls that can be evaluated to produce a value.

$a = 5; // assignment expression
$b = 10;

$sum = $a + $b; // arithmetic expression

echo "Sum: $sum\n"; // Sum: 15

if ($sum > 10) { // comparison expression
    echo "Sum is greater than 10\n"; // Sum is greater than 10
} else {
    echo "Sum is not greater than 10\n";
}
